//Write a program to check whether a number is even or odd.

public class CheckEvenOrOdd {
	public static void main(String[] args) {
		int num = 9;
		

		if (num % 2 == 0) {
			System.out.println(+ num+" is Even number");
		} else {
			System.out.println(+ num+ " Num is odd");
		}
	}
}
